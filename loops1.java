import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int i = sc.nextInt();
        for(int c=1;c<=10;c++)
        {
            System.out.println(i+" x "+c+" = "+i*c);
        }
    }
}
